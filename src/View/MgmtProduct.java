    private void purchaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseBtnActionPerformed
        if(table.getSelectedRow() >= 0){
            JTextField stockFld = new JTextField("0");
            designer(stockFld, "PRODUCT STOCK");
            
            Object selectedProduct = tableModel.getValueAt(table.getSelectedRow(), 0);

            Object[] message = {
                "How many " + selectedProduct + " do you want to purchase?", stockFld
            };

            int result = JOptionPane.showConfirmDialog(null, message, "PURCHASE PRODUCT", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, null);

            if (result == JOptionPane.OK_OPTION) {
                
                try {
                    // Get stock value from table
                    int availableStock = (int) tableModel.getValueAt(table.getSelectedRow(), 1);
                    
                    int quantity = Validator.validateQuantity(stockFld.getText(), availableStock);
                    
                    // Validate session
                    if (SessionManager.getSessionRole() != SessionManager.ROLE_CLIENT) {
                        JOptionPane.showMessageDialog(
                            this,
                            "Only clients are allowed to purchase products.",
                            "Access Denied",
                            JOptionPane.WARNING_MESSAGE
                        );
                        return; // Stop execution
                    }
                    
                    boolean res = sqlite.purchaseProduct(selectedProduct.toString(), quantity, SessionManager.getUsername());
                     
                    if (res) {
                        JOptionPane.showMessageDialog(this, "Purchase successful!");
                        init();
                    } else {
                        JOptionPane.showMessageDialog(this, "Purchase failed. Please try again.", "Database Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (IllegalArgumentException e) {
                    JOptionPane.showMessageDialog(
                        this,
                        e.getMessage(),
                        "Purchase Error",
                        JOptionPane.ERROR_MESSAGE
                    );
                }
            }
        }
    }//GEN-LAST:event_purchaseBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        JTextField nameFld = new JTextField();
        JTextField stockFld = new JTextField();
        JTextField priceFld = new JTextField();

        designer(nameFld, "PRODUCT NAME");
        designer(stockFld, "PRODUCT STOCK");
        designer(priceFld, "PRODUCT PRICE");

        Object[] message = {
            "Insert New Product Details:", nameFld, stockFld, priceFld
        };

        int result = JOptionPane.showConfirmDialog(null, message, "ADD PRODUCT", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, null);

        if (result == JOptionPane.OK_OPTION) {
            
            if (SessionManager.getSessionRole() < SessionManager.ROLE_STAFF) {
                JOptionPane.showMessageDialog(
                    this,
                    "You are not allowed to access this feature.",
                    "Access Denied",
                    JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            
            try {
                Validator.validateProductName(nameFld.getText());
                int stock = Validator.parsePositiveInt(stockFld.getText(), "Stock");
                BigDecimal price = Validator.validatePriceInput(priceFld.getText());

                sqlite.addProduct(nameFld.getText().trim(), stock, price.doubleValue(), SessionManager.getUsername());
                init();
                JOptionPane.showMessageDialog(this, "Product added successfully!");

            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Add Product Failed", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
    if (table.getSelectedRow() >= 0) {
            JTextField nameFld = new JTextField(tableModel.getValueAt(table.getSelectedRow(), 0) + "");
            JTextField stockFld = new JTextField(tableModel.getValueAt(table.getSelectedRow(), 1) + "");
            JTextField priceFld = new JTextField(tableModel.getValueAt(table.getSelectedRow(), 2) + "");

            designer(nameFld, "PRODUCT NAME");
            designer(stockFld, "PRODUCT STOCK");
            designer(priceFld, "PRODUCT PRICE");

            Object[] message = {
                "Edit Product Details:", nameFld, stockFld, priceFld
            };

            int result = JOptionPane.showConfirmDialog(
                null, message, "EDIT PRODUCT",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, null
            );

            if (result == JOptionPane.OK_OPTION) {

                if (SessionManager.getSessionRole() < SessionManager.ROLE_STAFF) {
                    JOptionPane.showMessageDialog(
                        this,
                        "You are not allowed to access this feature.",
                        "Access Denied",
                        JOptionPane.WARNING_MESSAGE
                    );
                    return;
                }

                try {
                    // Validate inputs
                    Validator.validateProductName(nameFld.getText());
                    int stock = Validator.parsePositiveInt(stockFld.getText(), "Stock");
                    BigDecimal price = Validator.validatePriceInput(priceFld.getText());

                    // Perform update
                    sqlite.updateProduct(
                        nameFld.getText().trim(),
                        stock,
                        price.doubleValue(),
                        SessionManager.getUsername()
                    );

                    init(); // Refresh table
                    JOptionPane.showMessageDialog(this, "Product updated successfully!");

                } catch (IllegalArgumentException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Edit Product Failed", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_editBtnActionPerformed

